public class Religion extends Nationality{
    private String christian;
    private String catholicism;
    private String crotestants;
    private String orthodoxChristianity;

    public String getChristian() {
        return christian;
    }

    public void setChristian(String christian) {
        this.christian = christian;
    }

    public String getCatholicism() {
        return catholicism;
    }

    public void setCatholicism(String catholicism) {
        this.catholicism = catholicism;
    }

    public String getCrotestants() {
        return crotestants;
    }

    public void setCrotestants(String crotestants) {
        this.crotestants = crotestants;
    }

    public String getOrthodoxChristianity() {
        return orthodoxChristianity;
    }

    public void setOrthodoxChristianity(String orthodoxChristianity) {
        this.orthodoxChristianity = orthodoxChristianity;
    }
}
