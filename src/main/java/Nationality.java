public abstract class Nationality extends People{
    private String germans;
    private String italians;
    private String english;
    private String finns;

    public String getGermans() {
        return germans;
    }

    public void setGermans(String germans) {
        this.germans = germans;
    }

    public String getItalians() {
        return italians;
    }

    public void setItalians(String italians) {
        this.italians = italians;
    }

    public String getEnglish() {
        return english;
    }

    public void setEnglish(String english) {
        this.english = english;
    }

    public String getFinns() {
        return finns;
    }

    public void setFinns(String finns) {
        this.finns = finns;
    }
}
